	/**
	 * Displays the neighbors of the displayed <code>Node</code>
	 */
	private void displayNeighbors() {
		Node researchedNode = graph.getNode(placeNameField.getText());
		int nbNeighbors = jumpNumberSlider.getValue();
		
		if (researchedNode != null && nbNeighbors >= 0) {
			List<NodeType> types = new ArrayList<>();
		
			if (recreationSelectorCheckBox.isSelected())
				types.add(NodeType.RECREATION);

			if (citySelectorCheckBox.isSelected())
				types.add(NodeType.CITY);

			if (restaurantSelectorCheckBox.isSelected())
				types.add(NodeType.RESTAURANT);


			List<Node> neighborsList = researchedNode.getNeighbors(nbNeighbors, linksFilter);

			canvas.setDisplayLinks(graph.extractDistinctLink(neighborsList));

			if (neighborExaclyRadioButton.isSelected())
				neighborsList = researchedNode.getExaclyNeighbors(nbNeighbors, linksFilter);

			canvas.setDisplayNodes(researchedNode.filterByType(neighborsList,types));
		}
	}
	
	/**
	 * Filters all the UI by adding or removing the <code>Link</code> type
	 * @param evt
	 * @param type 
	 */
	private void filterElements(ItemEvent evt, LinkType type) {
		if (evt.getStateChange() == ItemEvent.SELECTED) {
			if (!linksFilter.contains(type))
				linksFilter.add(type);
		} else {
			linksFilter.remove(type);
		}
		
		List<Link> links = new ArrayList<>();
		for (LinkType item: linksFilter)
			links.addAll(graph.getDistinctLinks(item));
		linksModel.setSelectedItem(null);
		linksModel.addAll(links);
		
		twoDistanceVerification();
		canvas.setDisplay(graph.getNodes(), graph.getDistinctLinks());
		canvas.repaint();
	}
	
	/**
	 * Filters all the UI by adding or removing the <code>Node</code> type
	 * @param evt
	 * @param type 
	 */
	private void filterElements(ItemEvent evt, NodeType type) {
		boolean isSelected = evt.getStateChange() == ItemEvent.SELECTED;
		if (isSelected) {
			if (!nodesFilter.contains(type))
				nodesFilter.add(type);
		} else {
			nodesFilter.remove(type);
			
			Node researchedNode = graph.getNode(placeNameField.getText());
			if (researchedNode != null && researchedNode.getType() == type) {
				placeNameField.setText("");
				placeCategoryField.setText("");
			}
			
			Node node = canvas.getSelected(0);
			if (node != null && node.getType() == type)
				canvas.addSelected(0, null);
			
			node = canvas.getSelected(1);
			if (node != null && node.getType() == type)
				canvas.addSelected(1, null);
		}
		
		List<Node> nodes = graph.getNodes(nodesFilter);
		Node selectedNode = (Node)firstNodeListModel.getSelectedItem();
		firstNodeListModel.addAll(nodes);
		if (selectedNode != null && nodesFilter.contains((selectedNode.getType())))
			firstNodeListModel.setSelectedItem(selectedNode);
		else {
			firstNodeListModel.setSelectedItem(null);
			firstChecked = false;
		}
		
		selectedNode = (Node)secondNodeListModel.getSelectedItem();
		secondNodeListModel.addAll(nodes);
		if (selectedNode != null && nodesFilter.contains((selectedNode.getType())))
			secondNodeListModel.setSelectedItem(selectedNode);
		else
			secondNodeListModel.setSelectedItem(null);
		
		switch(type) {
			case CITY:
				cityItinerarySpinner.setValue(0);
				((SpinnerNumberModel)cityItinerarySpinner.getModel()).setMaximum(isSelected ? graph.getNumberNodes(type) : 0);
				break;
			case RECREATION:
				recreationItinerarySpinner.setValue(0);
				((SpinnerNumberModel)recreationItinerarySpinner.getModel()).setMaximum(isSelected ? graph.getNumberNodes(type) : 0);
				break;
			case RESTAURANT:
				restaurantItinerarySpinner.setValue(0);
				((SpinnerNumberModel)restaurantItinerarySpinner.getModel()).setMaximum(isSelected ? graph.getNumberNodes(type) : 0);
				break;
		}
		
		canvas.setDisplay(graph.getNodes(), graph.getDistinctLinks());
		twoDistanceVerification();
		canvas.repaint();
	}
	
